    class ButtonEditor extends DefaultCellEditor {

        private JButton button;
        private boolean clicked;
        private int selectedRow;

        public ButtonEditor(JTextField textField) {
            super(textField);
            button = new JButton("");
            button.setIcon(new ImageIcon("D:\\DuAn1\\newPRJ\\5aesieunhan\\src\\icon\\delete_25px.png")); // Thêm icon nếu cần
            button.addActionListener(e -> deleteRow(selectedRow));
        }

        @Override
        public Component getTableCellEditorComponent(JTable table, Object value, boolean isSelected, int row, int column) {
            selectedRow = row;
            clicked = true;
            return button;
        }

        @Override
        public Object getCellEditorValue() {
            clicked = false;
            return null;
        }
    }

    private void deleteRow(int row) {
        int confirm = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn xóa khách hàng này?", "Xác nhận", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            int maKhachHang = (int) model.getValueAt(row, 1); // Lấy mã khách hàng từ dòng
            if (rp.deleteKhachHang(maKhachHang)) { // Gọi phương thức xóa trong repository
                JOptionPane.showMessageDialog(this, "Xóa khách hàng thành công!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                model.removeRow(row); // Xóa dòng khỏi bảng
            } else {
                JOptionPane.showMessageDialog(this, "Xóa khách hàng thất bại!", "Thông báo", JOptionPane.ERROR_MESSAGE);
            }
        }
    }

