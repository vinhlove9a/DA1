    private void fillToTable() {
        model.setRowCount(0);
        List<KhachHang> khachHangs = rp.getAllKhachHang();
        int stt = 1; // Biến đếm số thứ tự
        for (KhachHang kh : khachHangs) {
            model.addRow(new Object[]{
                stt++,
                kh.getMaKhachHang(),
                kh.getTenKhachHang(),
                kh.getSoDienThoai(),
                kh.isGioiTinh() ? "Nam" : "Nữ"
            });
        }
    }

    private void searchKhachHang() {
        String keyword = txtSearch.getText().trim();

        List<KhachHang> khachHangs;

        // Kiểm tra nếu ô tìm kiếm trống, load lại toàn bộ dữ liệu
        if (keyword.isEmpty()) {
            khachHangs = rp.getAllKhachHang();
        } else {
            khachHangs = rp.searchKhachHangByName(keyword);
        }

        // Cập nhật bảng
        model.setRowCount(0); // Xóa dữ liệu cũ
        int stt = 1;
        for (KhachHang kh : khachHangs) {
            model.addRow(new Object[]{
                stt++,
                kh.getMaKhachHang(),
                kh.getTenKhachHang(),
                kh.getSoDienThoai(),
                kh.isGioiTinh() ? "Nam" : "Nữ"
            });
        }
    }

    private void addKhachHang() {
        // Lấy thông tin từ form nhập liệu
        String tenKhachHang = txtTenKhachHang.getText().trim();
        String soDienThoai = txtSoDienThoai.getText().trim();
        boolean gioiTinh = rbNam.isSelected(); // Kiểm tra giới tính (Nam là true, nếu không là Nữ)

        // Kiểm tra thông tin hợp lệ
        if (tenKhachHang.isEmpty() || soDienThoai.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin!", "Thông báo", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Kiểm tra số điện thoại bắt đầu bằng 0 và có độ dài từ 10 đến 15 ký tự
        if (!soDienThoai.matches("^0\\d{9,14}$")) {  // Số điện thoại bắt đầu bằng 0 và có độ dài từ 10 đến 15 ký tự
            JOptionPane.showMessageDialog(this, "Số điện thoại không hợp lệ. Số điện thoại phải bắt đầu bằng 0 và có độ dài từ 10 đến 15 ký tự.", "Thông báo", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Kiểm tra giới tính (phải chọn Nam hoặc Nữ)
        if (!rbNam.isSelected() && !rbNu.isSelected()) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn giới tính!", "Thông báo", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Tạo đối tượng khách hàng
        KhachHang kh = new KhachHang(0, tenKhachHang, soDienThoai, gioiTinh);

        // Thêm khách hàng vào database
        if (rp.addKhachHang(kh)) {
            JOptionPane.showMessageDialog(this, "Thêm khách hàng thành công!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            fillToTable();  // Cập nhật bảng khách hàng
            txtTenKhachHang.setText("");  // Reset form
            txtSoDienThoai.setText("");   // Reset form
            rbNam.setSelected(true);      // Reset giới tính (Nam mặc định)
        } else {
            JOptionPane.showMessageDialog(this, "Thêm khách hàng thất bại!", "Thông báo", JOptionPane.ERROR_MESSAGE);
        }
    }

